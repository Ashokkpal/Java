//inverted half pyramid with number
public class o7_demo {
     public static void main(String... args) {
        for(int i=5;i>=1;i-=1){
            for(int j=1;j<=i;j+=1){
                System.out.print(j+" ");
            }System.out.println();
        }

     }}
     /*
1 2 3 4 5 
1 2 3 4
1 2 3
1 2
1
      */
         