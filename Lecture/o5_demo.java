//Q. Take 2 variable from user andprint their sum
import java.util.Scanner;

public class o5_demo {
    public static void main(String...args) {
        Scanner sc=new Scanner(System.in);
        System.out.print("Enter Fast Number:");
        int num1=sc.nextInt();
        System.out.print("Enter Second Number:");
        int num2=sc.nextInt();
        int sum=num1+num2;
        System.out.println("Total sum of "+num1+" and "+num2+" are "+sum);


        
    }
    
}
